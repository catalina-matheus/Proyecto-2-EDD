    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
      nuevoUsuario = inputUsername.getText(); 
      tipo = jList1.getSelectedValue(); 
        if (nuevoUsuario == null || tipo == null || nuevoUsuario.equals("") || nuevoUsuario.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe agregar el username y el tipo de usuario! Intente nuevamente"); 
        }else{
            NodeUsuario nuevo = new NodeUsuario(nuevoUsuario, tipo); 
            app.getLista().appendLast(nuevo);
            this.agregarUsuariosLista(); 
            JOptionPane.showMessageDialog(null, "El usuario fue agregado exitosamente!"); 
            
            // test del timer
           
            
            inputUsername.setText("");
            jList1.clearSelection();
            nuevoUsuario = null; 
            tipo = null; 
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
    
    
