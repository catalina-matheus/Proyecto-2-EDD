    public DefaultListModel limpiarList() {
        DefaultListModel modelo = new DefaultListModel();
        jList1.setModel(modelo);
        ListaUsuariosActuales.setModel(modelo); 
        return modelo;
    }

     /**
     * @Descripcion: funcion que coloca todos los usuarios que estan en el grafo
     * en la jList
     * @return
     * @author: Catalina Matheus
     * @version: 17/10/2023
     */
    public DefaultListModel agregarElementosLista() {
        DefaultListModel modelo = (DefaultListModel) jList1.getModel();
        String[] types = {"prioridad_alta", "prioridad_media", "prioridad_baja"};
         for (int i = 0; i < 3; i++) {
            modelo.addElement(types[i]);
        }

        return modelo;
    }
    public String[] getUserNames() {
        NodeUsuario aux = app.getLista().getpFirst();
        String[] Usernames = new String[app.getLista().getSize()];
        for (int i = 0; i < app.getLista().getSize(); i++) {
            Usernames[i] = aux.getNombreUsuario();
            aux = aux.getpNext();
        }
        return Usernames;
    }

     public DefaultListModel agregarUsuariosLista() {
       DefaultListModel modelo = new DefaultListModel();  
        NodeUsuario aux = app.getLista().getpFirst(); 
        while(aux != null) {
            modelo.addElement(aux.getNombreUsuario());
            aux = aux.getpNext();
        }
        ListaUsuariosActuales.setModel(modelo);
        return modelo; 
    }
    public boolean itExist(String[] nombres, String usuario) {
        boolean existe = false;
        for (int i = 0; i < nombres.length; i++) {
            if (nombres[i].equals(usuario)) {
                existe = true;
            }
        }
        return existe;

    }

