    private void ImprimirAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImprimirAllActionPerformed
        int tamano = app.getbMonticulo().getSize();
        NodeMonticulo eliminado = app.getbMonticulo().eliminar_min();
        if (eliminado != null) {

            NodeMonticulo[] listaEliminados = new NodeMonticulo[tamano];
            for (int i = 0; i < tamano; i++) {
                listaEliminados[i] = eliminado;
                eliminado = app.getbMonticulo().eliminar_min();
            }
            this.desplegarDocumentos(listaEliminados);
            this.sacarCola(listaEliminados);
        } else {
            JOptionPane.showMessageDialog(null, "No hay documentos en la cola de impresion");
        }


    }//GEN-LAST:event_ImprimirAllActionPerformed

    private void ImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImprimirActionPerformed
        // TODO add your handling code here:
        eliminados = Integer.parseInt(this.Eliminadosn.getText());
        if (eliminados != 0) {

            NodeMonticulo eliminado = app.getbMonticulo().eliminar_min();
            if (eliminado != null) {
                app.getbMonticulo().insertar(eliminado);
                NodeMonticulo[] listaEliminados = new NodeMonticulo[eliminados];
                for (int i = 0; i < eliminados; i++) {

                    eliminado = app.getbMonticulo().eliminar_min();
                    listaEliminados[i] = eliminado;
                }
                this.desplegarDocumentos(listaEliminados);
                this.Eliminadosn.setText("0");
                this.sacarCola(listaEliminados);
            } else {
                JOptionPane.showMessageDialog(null, "No hay documentos en la cola de impresion");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Tiene que seleccionar una cantidad de documentos para imprimir");
        }

    }//GEN-LAST:event_ImprimirActionPerformed

