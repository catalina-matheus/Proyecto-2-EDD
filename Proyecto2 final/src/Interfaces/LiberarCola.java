    /**
     * @Descripcion: Despliega los documentos eliminados en el Jlist
     * @param lista
     * @return
     * @version: 17/11/2023
     * @author: Reinaldo Reyes
     */
    public void desplegarDocumentos(NodeMonticulo[] lista) {
        DefaultListModel modelo = new DefaultListModel();
        String info;
        for (int i = 0; i < lista.length; i++) {
            NodeDocumento datos = lista[i].getData();
            info = "Nombre: " + datos.getNombre() + "    Tipo:" + datos.getTipo() + "   Tamaño:" + datos.getTamano();

            modelo.addElement(info);
        }

        this.ListImpresos.setModel(modelo);
    }


    /**
     * @Descripcion: Cambia el valor de verdad de los documentos, 
     * que ya no estan en la cola de impresion
     * @param lista
     * @return 
     * @version: 17/11/2023
     * @author: Reinaldo Reyes
     */
    public void sacarCola(NodeMonticulo[] lista){
        for (int i = 0; i < lista.length; i++) {
            lista[i].getData().setEnCola(false);
            app.getHashTable().EliminateDoc(lista[i]);
        }
    }

